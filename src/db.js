let db = null;
let pool = null;
const transactionStore = new AsyncLocalStorage();

if (isPostgres) {
  if (!DATABASE_URL) {
    throw new Error("DATABASE_URL is required when DATABASE_PROVIDER=postgres.");
  }

  pool = new Pool({
    connectionString: DATABASE_URL,
    ssl: DATABASE_SSL || undefined,
  });

  pool.on("error", (error) => {
    console.error("Unexpected Postgres pool error:", error);
  });
} else {
  db = new DatabaseSync(DB_PATH);
  db.exec("PRAGMA foreign_keys = ON;");
  db.exec("PRAGMA journal_mode = WAL;");
}

function nowIso() {
  return new Date().toISOString();
}

function compileNamedQuery(sql, params = {}) {
  const values = [];
  const statement = sql.replace(/:([a-zA-Z_][a-zA-Z0-9_]*)/g, (_match, key) => {
    values.push(Object.prototype.hasOwnProperty.call(params, key) ? params[key] : null);
    return `$${values.length}`;
  });

  return {
    statement,
    values,
  };
}

function appendReturningId(sql) {
  if (!/^\s*insert\b/i.test(sql) || /\breturning\b/i.test(sql)) {
    return sql;
  }

  return `${sql.trim().replace(/;$/, "")} RETURNING id`;
}

function extractAliases(sql) {
  const aliases = new Map();
  const pattern = /\bAS\s+("?)([a-zA-Z_][a-zA-Z0-9_]*)\1/gi;
  let match = pattern.exec(sql);

  while (match) {
    aliases.set(match[2].toLowerCase(), match[2]);
    match = pattern.exec(sql);
  }

  return aliases;
}

function normalizePostgresRow(row, aliases) {
  if (!row || aliases.size === 0) {
    return row;
  }

  const normalized = { ...row };
  for (const [key, value] of Object.entries(row)) {
    const alias = aliases.get(key.toLowerCase());
    if (alias && alias !== key) {
      normalized[alias] = value;
    }
  }

  return normalized;
}

async function executePostgres(sql, params = {}, { mode } = {}) {
  const executor = transactionStore.getStore() || pool;
  const compiled = compileNamedQuery(mode === "run" ? appendReturningId(sql) : sql, params);
  const result = await executor.query(compiled.statement, compiled.values);
  const aliases = extractAliases(sql);

  if (mode === "run") {
    return {
      rowCount: result.rowCount,
      lastInsertRowid: result.rows[0]?.id ?? 0,
    };
  }

  if (mode === "get") {
    return normalizePostgresRow(result.rows[0], aliases) || null;
  }

  return result.rows.map((row) => normalizePostgresRow(row, aliases));
}

async function run(sql, params = {}) {
  if (isPostgres) {
    return executePostgres(sql, params, { mode: "run" });
  }

  return db.prepare(sql).run(params);
}

async function get(sql, params = {}) {
  if (isPostgres) {
    return executePostgres(sql, params, { mode: "get" });
  }

  return db.prepare(sql).get(params);
}

async function all(sql, params = {}) {
  if (isPostgres) {
    return executePostgres(sql, params, { mode: "all" });
  }

  return db.prepare(sql).all(params);
}

async function withTransaction(work) {
  if (isPostgres) {
    const client = await pool.connect();

    try {
      await client.query("BEGIN");
      const result = await transactionStore.run(client, async () => work());
      await client.query("COMMIT");
      return result;
    } catch (error) {
      await client.query("ROLLBACK");
      throw error;
    } finally {
      client.release();
    }
  }

  db.exec("BEGIN");
  try {
    const result = await work();
    db.exec("COMMIT");
    return result;
  } catch (error) {
    db.exec("ROLLBACK");
    throw error;
  }
}

function text(value) {
  return typeof value === "string" ? value.trim() : "";
}

function lowerEmail(value) {
  return text(value).toLowerCase();
}

function toInt(value) {
  const parsed = Number.parseInt(value, 10);
  return Number.isFinite(parsed) ? parsed : null;
}

function isRole(value) {
  return ["student", "instructor", "admin"].includes(value);
}

function isStatus(value) {
  return ["pending", "approved", "rejected"].includes(value);
}

function slugify(value) {
  return (
    text(value)
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .slice(0, 48) || "file"
  );
}

async function initializeDatabase() {
  if (isPostgres) {
    await pool.query(POSTGRES_SCHEMA);
    return;
  }

  db.exec(SQLITE_SCHEMA);
}

